<a name="Description" id="Description"></a><h2>Description</h2>
<p>The <b>wpcf_form_fields</b> filter is used to filter configuration array on
the "Edit Group" screen.</p>

<a name="Parameters" id="Parameters"></a><h2>Parameters</h2>
<dl>
    <dt><tt>$fields</tt></dt>
    <dd>(<i>array</i>) (<i>required</i>) Existing fields to be filtered.
    <dl>
        <dd>Default: <i>None</i></dd>
    </dl>
    </dd>
    <dt><tt>$group</tt></dt>
    <dd>(<i>array</i>) edited group data.
    <dl>
        <dd>Default: <i>None</i></dd>
    </dl>
    </dd>
</dl>

<a name="Return_Values" id="Return_Values"></a><h2>Return Values</h2>
<dl>
    <dt><tt>(array)</tt></dt>
    <dd>Filtered fields data.</dd>
</dl>

<a name="Source_File" id="Source_File"></a><h2>Source File</h2>
<p>wpcf_form_fields is located in: includes/fields-form.php.</p>

