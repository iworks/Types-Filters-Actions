<a name="Description" id="Description"></a><h2>Description</h2>
<p>The <b>wpcf_fields_form_additional_filters</b> filter is used to add data
to the fields on the "Edit Group" screen. This filter can not change anything.</p>

<a name="Parameters" id="Parameters"></a><h2>Parameters</h2>
<dl>
    <dt><tt>$classes</tt></dt>
    <dd>(<i>array</i>) (<i>required</i>) empty array as entry.
    <dl>
        <dd>Default: <i>None</i></dd>
    </dl>
    </dd>
    <dt><tt>$group</tt></dt>
    <dd>(<i>array</i>) edited group data.
    <dl>
        <dd>Default: <i>None</i></dd>
    </dl>
    </dd>
</dl>

<a name="Return_Values" id="Return_Values"></a><h2>Return Values</h2>
<dl>
    <dt><tt>(array)</tt></dt>
    <dd>Array with values which enhance fields data.</dd>
</dl>


